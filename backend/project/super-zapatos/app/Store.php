        |--------------------------------------------------------------------------
        | GLOBAL VARIABLES
        |--------------------------------------------------------------------------
        */

        protected $table = 'stores';
        protected $primaryKey = 'id';
        protected $hidden = ['id'];
        protected $fillable = [
                                'name',
                                'address',
                              ];
        public $timestamps = true;

        /*
        |--------------------------------------------------------------------------
        | FUNCTIONS
        |--------------------------------------------------------------------------
        */

        /*
        |--------------------------------------------------------------------------
        | RELATIONS
        |--------------------------------------------------------------------------
        */

        public function articles()
        {
            return $this->hasMany('App\Article');
        }

